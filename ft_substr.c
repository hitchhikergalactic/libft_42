#include "libft.h"

char	*ft_substr(const char *s, unsigned int start, size_t len)
{
	char	*new_str;
	size_t	i;
	size_t	j;
	size_t	slen;

	if (!s)
		return (NULL);
	slen = ft_strlen(s);
	if (slen <= start)
		return (ft_strdup("\0"));
	if (start + len > slen)
		len = slen - start;
	new_str = (char *)malloc(len + 1);
	if (!new_str)
		return (NULL);
	i = start;
	j = 0;
	while (i < (size_t)(intmax_t)slen && j < len)
	{
		new_str[j] = s[i];
		j++;
		i++;
	}
	new_str[j] = '\0';
	return (new_str);
}
